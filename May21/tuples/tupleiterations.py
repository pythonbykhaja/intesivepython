rainbow = ('Violet', 'Indigo', 'Blue', 'Green', 'Yellow', 'Orange', 'Red',)
index = 0
# Iterating using while
print('Iteration using while')
while index < len(rainbow):
    print(rainbow[index])
    index += 1

# Iterating using for in
print('Iteration using for in')
for color in rainbow:
    print(color)



