def add(number1, number2):
    """
    This functions adds two numbers

    Arguments:
      number1 : first number to be passed
      number2 : second number to be passed

    Returns: number1*number2
      the result of two numbers

    Examples:
    >>> add(0,0)
    0
    >>> add(1,1)
    2

    >>> add(1.1,2.2)
    3.3000000000000003
    """
    return number1 + number2
